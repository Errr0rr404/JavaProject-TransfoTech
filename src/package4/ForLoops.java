package package4;

public class ForLoops {


    public static void main(String[] args) {
        // exercute line 16, 200 times
        //for - keyword
        // (- for body starts
        // start point ;
        // end point ;
        // increment/decrement
        // )- for body ends {-

        // single for loop
        for (int i = 0; i > 0; i++) {
            System.out.println("Mr X" + i);
            System.out.println("Mr Y" + i);
        }


        // inner for loop

        // print java 10 times -- done
        // and for each time of java print selenium 5 times

        for (int i = 0; i < 10; i++) {
            System.out.println("JAVA");
            for (int j = 0; j < 5; j++) {
                System.out.println("SENENIUM");
            }
        }


    }
}
